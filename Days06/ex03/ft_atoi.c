#include <stdlib.h>
#include <stdio.h>

int		ft_atoi(char *str)
{
	int i;
	int moins;
	int b;
	int res;

	res = 0;
	b = 1;
	moins = 0;
	i = 0;
	while (str[i] == '\t' || str[i] == '\n' || str[i] == '\v'
			|| str[i] == '\f' || str[i] == '\r' || str[i] == ' ')
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			moins++;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + (str[i] - '0');
		i++;
	}
	return (res * ((moins % 2) ? -1 : 1));
}
