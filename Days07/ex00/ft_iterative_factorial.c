int		ft_iterative_factorial(int c)
{
	int res;

	if (c < 0 || c > 12)
		return (0);
	if (c == 0 || c == 1)
		return (1);
	res = 1;
	c++;
	while (c-- > 1)
		res *= c;
	return (res);
}
