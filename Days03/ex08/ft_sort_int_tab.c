void	ft_sort_int_tab(int *tab, int size)
{
	int i;
	int tmp;

	i = 0;
	while (i + 1 < size)
	{
		i++;
		if (tab[i - 1] > tab[i])
		{
			tmp = tab[i];
			tab[i] = tab[i - 1];
			tab[i - 1] = tmp;
			i = 0;
		}
	}
}
